const Marketplaceaddress = "0x5FbDB2315678afecb367f032d93F642f64180aa3";
const BasicNftaddress = "0xe7f1725E7734CE288F8367e1Bb143E90bb3F0512";
const PRICE = ethers.utils.parseEther("0.1");
async function mintandList() {
  const basicnft = new ethers.Contract(
    Marketplaceaddress,
    MarketplaceAbi,
    ethers.provider
  );
  const nftmarketplace = new ethers.Contract(
    BasicNftaddress,
    BasicNftAbi,
    ethers.provider
  );
  console.log("minting NFT....");
  const mintTx = await basicnft.mintNft();
  const mintReciept = await mintTx.wait(1);
  const tokenId = await mintReciept.events[0].args.tokenId;
  console.log("Approving nft....");
  const ApproveTx = await basicnft.approve(nftmarketplace.address, tokenId);
  await ApproveTx.wait(1);
  console.log("Approved....");
  console.log("Listing NFT....");
  const ListTx = await nftmarketplace.ListNfts(
    basicnft.address,
    tokenId,
    PRICE
  );
  await ListTx.wait(1);
  console.log("Listed....");
}

mintandList()
  .then(() => process.exit(0))
  .catch((err) => {
    console.log(err);
    process.exit(1);
  });
